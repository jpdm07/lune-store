export function getReviewsByProductSlug(slug) {
  return REVIEWS.filter((r) => r.slug === slug)
}

export function getReviewStatsForProduct(slug) {
  const list = getReviewsByProductSlug(slug)
  if (list.length === 0) return { avg: null, count: 0 }
  const sum = list.reduce((acc, r) => acc + r.rating, 0)
  const avg = Math.round((sum / list.length) * 10) / 10
  return { avg, count: list.length }
}

export function formatReviewDate(iso) {
  try {
    return new Date(iso + 'T12:00:00').toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'short',
      day: 'numeric',
    })
  } catch {
    return iso
  }
}
